"""Pluggable document-driven coding providers.

The default provider integrates the official OpenSpec Codex skills generated by
`openspec init --tools codex`. It does not reimplement OpenSpec command
semantics; it loads the generated skill instructions and asks the coding LLM to
execute those instructions for each planned task.
"""

